    private void termekKerekGombActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_termekKerekGombActionPerformed
        if (modositRadioGomb.isSelected()) {
            try {

                Termék termek = db.lekerdezTermek(cikkszam.getText());
                int cegId = termek.getBeszallitoId();
                megnevezes.setText(termek.getMegnevezes());
                ar.setText(String.valueOf(termek.getAr()));
                megnevezes.setEditable(true);
                megnevezes.setBackground(Color.WHITE);
                ar.setEditable(true);
                ar.setBackground(Color.WHITE);
                beszallitoLista.setEnabled(true);
                beszallitoLista.setSelectedItem(db.lekerdezBeszallito(cegId));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Nincs ilyen termék!");
            }
        }
        if (torolRadioGomb.isSelected()) {
            try {
                Termék termek = db.lekerdezTermek(cikkszam.getText());
                int cegId = termek.getBeszallitoId();
                megnevezes.setText(termek.getMegnevezes());
                ar.setText(String.valueOf(termek.getAr()));
                beszallitoLista.setSelectedItem(db.lekerdezBeszallito(cegId));

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Nincs ilyen termék!");
            }
        }
    }//GEN-LAST:event_termekKerekGombActionPerformed

