    private void ujTermekHozzaadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ujTermekHozzaadActionPerformed
        try {
            int cegId_ = ((Beszállító) beszallitoLista.getSelectedItem()).getCegId();
            String cikkszam_ = cikkszam.getText();
            String megnevezes_ = megnevezes.getText();
            int ar_ = Integer.parseInt(ar.getText());

            if (cikkszam_.equals("") || megnevezes_.equals("") || ar.getText() == "") {
                JOptionPane.showMessageDialog(null, "Az adatok megadása kötelező!");
                return;
            }
            Termék termek = new Termék(cegId_, cikkszam_, megnevezes_, ar_);

            if (hozzaadRadioGomb.isSelected()) {
                db.ujTermek(termek);
            }
            if (modositRadioGomb.isSelected()) {
                db.modositTermek(termek);
            }
            if (torolRadioGomb.isSelected()) {
                db.torolTermek(termek);
            }
            cikkszam.setText("");
            megnevezes.setText("");
            ar.setText("");
        } catch (Exception e) {
           JOptionPane.showMessageDialog(null, "Az adatok megadása kötelező!"); 
        }


    }//GEN-LAST:event_ujTermekHozzaadActionPerformed

